#!/bin/bash

# Install React dependencies
npm install react-helmet --save
npm install --save-dev @types/react-helmet

# Install Leaflet for maps
npm install leaflet react-leaflet --save

# Install React Icons
npm install react-icons --save

# Install React Query for data fetching
npm install @tanstack/react-query --save

# Install React Hook Form for form handling
npm install react-hook-form --save

# Install Axios for HTTP requests
npm install axios --save

# Install React Router
npm install react-router-dom --save

# Install React Slick for carousels
npm install react-slick slick-carousel --save

# Install other utilities
npm install classnames --save

echo "All dependencies installed successfully!" 